#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int i;

	i = 0;
	while (*s)
	{
		if (*(s + i) == (const char)c)
		{
			return ((char*)(s + i));
		}
		++i;
	}
	return (NULL);
}
