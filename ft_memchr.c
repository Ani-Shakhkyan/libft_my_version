#include "libft.h"
#include <string.h>
#include <stdio.h>

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t i;

	i = 0;
	while (i < n)
	{
		if (*(const char *)(s + i) == (const char)c)
		{
			return ((char*)(s + i));
		}
		++i;
	}
	return (NULL);
}
